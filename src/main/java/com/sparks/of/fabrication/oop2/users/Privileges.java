package com.sparks.of.fabrication.oop2.users;

/**
 * Enum representing various privileges assigned to users within the system.
 */
public enum Privileges {

    /**
     * Privilege allowing a user to act as a cashier.
     */
    CASHIER,

    /**
     * Privilege allowing a user to manage inventory.
     */
    MANAGE_INVENTORY,

    /**
     * Privilege allowing a user to view reports.
     */
    VIEW_REPORTS,

    /**
     * Privilege granting administrative rights.
     */
    ADMIN_PRIVILEGES;
}
